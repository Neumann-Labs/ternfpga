#!/usr/bin/env python3
"""Emit the throughput-harness test vector as a C header.

  python soc/firmware/gen_testvec_bench.py > soc/firmware/testvec_bench.h

A larger GEMV (NT=64, M=64 -> 4096 tiles) so the measured cycle_count cleanly shows the
engine's ~1 tile/cycle rate on silicon. Same golden + packing as the RTL tests.
"""
import os
import sys

sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", "..", "models"))
from ternary_ref import pack_activations, pack_weights, ternary_gemv_golden  # noqa: E402

K, NT, M = 8, 63, 63          # KT = 504, 3969 weight tiles (nt/m <= 63 fit the 6-bit CSRs)
KT = K * NT
import random
rng = random.Random(4242)
x = [rng.randint(-30, 30) for _ in range(KT)]
W = [[rng.choice([-1, 0, 1]) for _ in range(KT)] for _ in range(M)]

X = [pack_activations(x[t * K:(t + 1) * K]) for t in range(NT)]                # NT uint64
WT = [pack_weights(W[m][t * K:(t + 1) * K]) for m in range(M) for t in range(NT)]  # M*NT uint16
exp = ternary_gemv_golden(W, x)

print("// auto-generated by gen_testvec_bench.py — throughput-harness test vector")
print(f"#define TV_K {K}")
print(f"#define TV_NT {NT}")
print(f"#define TV_M {M}")
print(f"static const unsigned long long X[{NT}] = {{{', '.join(f'0x{v:016x}ULL' for v in X)}}};")
print(f"static const unsigned short WT[{len(WT)}] = {{{', '.join(str(v) for v in WT)}}};")
print(f"static const int EXP[{M}] = {{{', '.join(str(v) for v in exp)}}};")
